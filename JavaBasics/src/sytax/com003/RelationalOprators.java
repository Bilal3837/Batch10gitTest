package sytax.com003;

public class RelationalOprators {

	public static void main(String[] args) {
		
		// Relational operators: >, >=, <=, ==, !=,
		
		
		int num=10;
		int num1=20;
		
		System.out.println(num>=num1);
		System.out.println(num!=num1);
		System.out.println(num<num);
		
	int a=100;
	int b=190;
	
	boolean boo=a<b; // result of the relational Operators Always a boolean value(true or false
	
	System.out.println(boo);
	System.out.println("--------------");
	
	System.out.println(a==b); // Checking for equality
	System.out.println(a=b); // reassigning value of Variable a to b
	
	System.out.println(a<=b);
	
	System.out.println(a<b);
	
	
	
	
	
		
		
		

	}

}
